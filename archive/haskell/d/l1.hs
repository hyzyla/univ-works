{-- проходимося list compehension по списку кортежів, 
де перший елемент це номер по порядку, у нашому випадку це індекс, 
а другий елемент це наш сам елемент, 
у умовному виразі відбираємо лише ті елементи, 
що дорівнюють максимальному значенню списку і повертаємо лише ідекси, 
що задовільняють даній умові --}

task :: [Int] -> [Int]
task xs = [i | (i, x) <- zip [1..] xs, x == maximum xs ]

main = do
    print $ task [2,2,3,4,5,1,2,3,3,3,1,2,3,5,5,3]